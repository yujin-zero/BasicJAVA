public class ArithOperTest_01 {
    public static void main(String[] args) {
        int x = 100, y = 200;

        int addValue = x + y;
        int subtractValue = x - y;
        int multiValue = x * y;
        int divValue = x / y;
        int modValue = x % y;

        System.out.println(addValue);
        System.out.println(subtractValue);
        System.out.println(multiValue);
        System.out.println(divValue);
        System.out.println(modValue);
    }
}
